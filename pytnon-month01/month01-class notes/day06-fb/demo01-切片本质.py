#列表元素可变
list01 = ['a','b','c']
list01[0] = ['A']  #索引,表示一个值
#将右边列表的值赋值给list01的第一个位置,
print('索引赋值:',list01)#索引赋值: [['A'], 'b', 'c']
#序列赋值
# name,age = '龙王',89
list01[1:2] = ['哪吒','敖丙'] #切片,表示一系列值
print('序列赋值:',list01)#序列赋值: [['A'], '哪吒', '敖丙', 'c']

#列表反向:
list02 = list01[::-1]
print('反向列表:',list02)#反向列表: ['c', '敖丙', '哪吒', ['A']]
#地址判断(变量赋值本质是引用)
print(list01[0] is list02[-1])#True
list01[0] = '李靖'
print('切片后赋值:',list02)#切片后赋值: ['c', '敖丙', '哪吒', ['A']]
